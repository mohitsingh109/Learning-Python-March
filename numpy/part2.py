# Special Function

# Array Fill with 0's
# Array Fill with 1's

import numpy as np

zero_array = np.zeros((2, 2))
print("Zero:", zero_array)

one_array = np.ones((2, 2))
print("Ones:", one_array)

empty_array = np.empty((2, 2))
print("Empty:", empty_array)

range_array = np.arange(0, 10, 2)
print("Range:", range_array)

eye_array = np.eye(3)
print("Eye:", eye_array)

linespace_array = np.linspace(0, 1, 6)
print("Linespace:", linespace_array)